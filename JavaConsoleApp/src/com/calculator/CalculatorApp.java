package com.calculator;

public class CalculatorApp {
	
	public int sum(int num1, int num2) {
		int result;
		result = num1 + num2;
		return result;
	}
	
	public int substraction(int num1, int num2) {
		int result;
		result = num1 - num2;
		return result;
	}
	
	public int multiply(int num1, int num2) {
		int result;
		result = num1*num2;
		return result;
	}
	
	public int divide(int num1, int num2) {
		int result;
		result = num1 / num2;
		return result;
	}

}
